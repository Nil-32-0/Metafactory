ServerEvents.recipes(event => {
    event.recipes.gtceu.comet("dirty")
    .itemInputs(
        "1x minecraft:stone",
        "2x minecraft:dirt"
    )
    .inputFluids(
        Fluid.of("minecraft:water", 500)
    )
    .chancedOutput("minecraft:diamond", 1000, 0)
    .duration(1000)
    .EUt(GTValues.VA[GTValues.OpV])
})